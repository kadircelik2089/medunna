package pojo;

public class RequestMessageResponseItem{
	private String subject;
	private String name;
	private int id;
	private String message;
	private String email;

	public String getSubject(){
		return subject;
	}

	public String getName(){
		return name;
	}

	public int getId(){
		return id;
	}

	public String getMessage(){
		return message;
	}

	public String getEmail(){
		return email;
	}
}
